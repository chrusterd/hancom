a = 5
b = 2
print(f"a = {a}, b = {b}")
print(f"a + b = {a + b}")  # 7
print(f"a - b = {a - b}")  # 3
print(f"a * b = {a * b}")  # 10
print(f"a / b = {a / b}")  # 2.5
print(f"a // b = {a // b}")  # 2
print(f"a % b = {a % b}")  # 1
print(f"a ** b = {a ** b}")  # 25
print(f"a == b = {a == b}")  # False
print(f"a != b = {a != b}")  # True
print(f"a > b = {a > b}")  # True
print(f"a < b = {a < b}")  # False
print(f"a >= b = {a >= b}")  # True
print(f"a <= b = {a <= b}")  # False    
print(f"a and b = {a and b}")  # 2
print(f"a or b = {a or b}")  # 5
print(f"not a = {not a}")  # False

